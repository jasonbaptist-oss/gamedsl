(* tests/unit/test_codegen.ml
   OUnit2 unit tests for codegen.ml.
   Strategy: build small AST fragments by hand (same approach as
   test_semant.ml), run them through codegen's emit_* functions,
   and assert the resulting Python source contains the expected
   substring. This checks codegen structurally without needing a
   Python interpreter in the OCaml test process.

   A SEPARATE Python-side script (verify_codegen.py, included
   alongside) takes the .py files generated by `generate_to_file`
   and confirms they are syntactically valid Python using Python's
   own ast.parse — that is the right tool to validate generated
   Python, not OCaml string matching.

   Run:
     ocamlfind ocamlopt -package ounit2 -linkpkg \
       compiler/lexer/lexer.ml ast.ml parser.ml semant.ml codegen.ml \
       tests/unit/test_codegen.ml -o run_codegen_tests
     ./run_codegen_tests
*)

open OUnit2
