  let c = CAnd (CCmp (EVar "x", Gt, EInt 0), CCmp (EVar "y", Lt, EInt 10)) in
  assert_equal "((x > 0) and (y < 10))" (emit_condition c)

let test_emit_condition_or _ =
  let c = COr (CCmp (EVar "x", Eq, EInt 1), CCmp (EVar "x", Eq, EInt 2)) in
  assert_equal "((x == 1) or (x == 2))" (emit_condition c)

let test_emit_condition_not _ =
  let c = CNot (CCmp (EVar "x", Eq, EInt 0)) in
  assert_equal "(not (x == 0))" (emit_condition c)

