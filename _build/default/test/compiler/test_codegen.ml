let test_emit_int _ =
  assert_equal "42" (emit_expr (EInt 42))

let test_emit_float _ =
  assert_equal "3.14" (emit_expr (EFloat 3.14))

let test_emit_var _ =
  assert_equal "wave" (emit_expr (EVar "wave"))

let test_emit_binop_add _ =
  assert_equal "(2 + 3)" (emit_expr (EBinop (Add, EInt 2, EInt 3)))

let test_emit_binop_nested_precedence _ =
  (* (base * 2) + 1 — parens make precedence explicit regardless
     of Python's own precedence rules, so nesting is always safe *)
  let e = EBinop (Add, EBinop (Mul, EVar "base", EInt 2), EInt 1) in
  assert_equal "((base * 2) + 1)" (emit_expr e)

let test_emit_runtime_call_with_string_arg _ =
  let e = ERuntimeCall (MonstersKilled, Some (ArgString "Goblin")) in
  assert_equal "game_state.monster_killed_count(\"Goblin\")" (emit_expr e)

let test_emit_runtime_call_with_ident_arg _ =
  let e = ERuntimeCall (MonsterCount, Some (ArgIdent "name")) in
  assert_equal "game_state.monster_count(name)" (emit_expr e)

let test_emit_runtime_call_no_arg _ =
  assert_equal "game_state.players_alive" (emit_expr (ERuntimeCall (PlayersAlive, None)))

let test_emit_field_read_literal_entity _ =
  let e = EFieldRead (RefString "Goblin", [ "health" ]) in
  assert_equal "entity_registry[\"Goblin\"].health" (emit_expr e)

let test_emit_field_read_param_entity _ =
  let e = EFieldRead (RefIdent "name", [ "health" ]) in
  assert_equal "entity_registry[name].health" (emit_expr e)

